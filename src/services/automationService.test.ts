describe('複数段 + 複数ブロック構成の総合テスト', () => {
    const products = [
        makeProduct('p1', '牛肉A', 100, 1),
        makeProduct('p2', '牛肉B', 100, 2),
        makeProduct('p3', '豚肉A', 100, 10),
        makeProduct('p4', '豚肉B', 100, 15),
        makeProduct('p5', '鶏肉A', 100, 20),
    ];

    it('5段棚にブロック3つ、ルールAで段ごとに独立カット', () => {
        const stdProducts: StandardPlanogramProduct[] = [];
        // 5段 x 5商品 = 25商品
        for (let shelf = 0; shelf < 5; shelf++) {
            for (let i = 0; i < 5; i++) {
                stdProducts.push(
                    makeStdProduct(`s${shelf}_${i}`, `p${i + 1}`, shelf, i * 100, 1)
                );
            }
        }
        // 標準幅500mm → ターゲット300mm
        const result = applyRuleA(stdProducts, products, 300);

        // 各段3商品に削減
        for (let shelf = 0; shelf < 5; shelf++) {
            const shelfProducts = result.products.filter(p => p.shelfIndex === shelf);
            expect(shelfProducts.length).toBeLessThanOrEqual(3);

            const shelfWidth = shelfProducts.reduce((sum, sp) => sum + 100 * sp.faceCount, 0);
            expect(shelfWidth).toBeLessThanOrEqual(300);
        }
    });

    it('5段棚にブロック3つ、ルールBで段ごとに独立拡張', () => {
        const stdProducts: StandardPlanogramProduct[] = [];
        for (let shelf = 0; shelf < 5; shelf++) {
            for (let i = 0; i < 3; i++) {
                stdProducts.push(
                    makeStdProduct(`s${shelf}_${i}`, `p${i + 1}`, shelf, i * 100, 1)
                );
            }
        }
        // 標準幅300mm → ターゲット600mm
        const result = applyRuleB(stdProducts, products, 600, 300);

        for (let shelf = 0; shelf < 5; shelf++) {
            const shelfProducts = result.products.filter(p => p.shelfIndex === shelf);
            const shelfWidth = shelfProducts.reduce((sum, sp) => sum + 100 * sp.faceCount, 0);
            expect(shelfWidth).toBeLessThanOrEqual(600);
        }

        // p1(ランク1)は全段で拡張
        const p1Products = result.products.filter(p => p.productId === 'p1');
        expect(p1Products.every(p => p.faceCount >= 2)).toBe(true);
    });
});
