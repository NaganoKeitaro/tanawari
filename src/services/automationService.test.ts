// ================================================================
// テストヘルパー
// ================================================================

function makeProduct(id: string, name: string, width: number, salesRank: number): Product {
    return {
        id,
        name,
        jan: `49000000${id}`,
        width,
        height: 200,
        depth: 100,
        salesRank,
        createdAt: '',
        updatedAt: '',
    } as Product;
}

function makeStdProduct(
    id: string,
    productId: string,
    shelfIndex: number,
    positionX: number,
    faceCount: number,
    placedBlockId?: string
): StandardPlanogramProduct {
    return { id, productId, shelfIndex, positionX, faceCount, placedBlockId };
}

// ================================================================
// ルールA: カット処理 (店舗の棚 < 標準)
// ================================================================

function applyRuleA(
    products: StandardPlanogramProduct[],
    productMaster: Product[],
    targetWidth: number
): { products: StorePlanogramProduct[]; warnings: string[] } {
    const warnings: string[] = [];

    const sortedProducts = [...products].sort((a, b) => {
        const prodA = productMaster.find(p => p.id === a.productId);
        const prodB = productMaster.find(p => p.id === b.productId);
        return (prodB?.salesRank || 100) - (prodA?.salesRank || 100);
    });

    const result: StorePlanogramProduct[] = [];
    const shelfIndices = [...new Set(sortedProducts.map(p => p.shelfIndex))];

    for (const shelfIndex of shelfIndices) {
        let shelfProducts = sortedProducts.filter(p => p.shelfIndex === shelfIndex);
        let currentWidth = 0;

        for (const sp of shelfProducts) {
            const product = productMaster.find(p => p.id === sp.productId);
            if (!product) continue;
            currentWidth += product.width * sp.faceCount;
        }

        while (currentWidth > targetWidth && shelfProducts.length > 0) {
            const worstProduct = shelfProducts[0];
            const product = productMaster.find(p => p.id === worstProduct.productId);

            if (!product) {
                shelfProducts = shelfProducts.slice(1);
                continue;
            }

            if (worstProduct.faceCount > 1) {
                worstProduct.faceCount -= 1;
                currentWidth -= product.width;
                warnings.push(`${product.name}: フェイス数を削減 (${worstProduct.faceCount + 1} → ${worstProduct.faceCount})`);
            } else {
                shelfProducts = shelfProducts.slice(1);
                currentWidth -= product.width;
                warnings.push(`${product.name}: カット（売上ランク ${product.salesRank}）`);
            }
        }

        let posX = 0;
        for (const sp of shelfProducts) {
            const product = productMaster.find(p => p.id === sp.productId);
            if (!product) continue;

            result.push({
                id: `gen-${sp.id}`,
                productId: sp.productId,
                shelfIndex: sp.shelfIndex,
                positionX: posX,
                faceCount: sp.faceCount,
                isAutoGenerated: true,
                isCut: false,
            });
            posX += product.width * sp.faceCount;
        }
    }

    return { products: result, warnings };
}

// ================================================================
// ルールB: 拡張処理 (店舗の棚 > 標準)
// ================================================================

function applyRuleB(
    products: StandardPlanogramProduct[],
    productMaster: Product[],
    targetWidth: number,
    _standardWidth: number
): { products: StorePlanogramProduct[]; warnings: string[] } {
    const warnings: string[] = [];

    const rankedProducts = products
        .map(p => ({
            ...p,
            salesRank: productMaster.find(pm => pm.id === p.productId)?.salesRank || 100,
        }))
        .sort((a, b) => a.salesRank - b.salesRank)
        .slice(0, 10);

    const top10Ids = new Set(rankedProducts.map(p => p.productId));

    const shelfIndices = [...new Set(products.map(p => p.shelfIndex))];
    const result: StorePlanogramProduct[] = [];

    for (const shelfIndex of shelfIndices) {
        const shelfProducts = products
            .filter(p => p.shelfIndex === shelfIndex)
            .map(p => ({ ...p }));

        let currentUsed = 0;
        for (const sp of shelfProducts) {
            const product = productMaster.find(p => p.id === sp.productId);
            if (product) currentUsed += product.width * sp.faceCount;
        }

        // 1st pass: 2倍
        for (const sp of shelfProducts) {
            if (!top10Ids.has(sp.productId)) continue;
            const product = productMaster.find(p => p.id === sp.productId);
            if (!product) continue;

            const extraNeeded = product.width * sp.faceCount;
            const available = targetWidth - currentUsed;

            if (available >= extraNeeded) {
                const oldFace = sp.faceCount;
                sp.faceCount = oldFace * 2;
                currentUsed += product.width * oldFace;
                warnings.push(`${product.name}: フェイス2倍 (${oldFace} → ${sp.faceCount})`);
            }
        }

        // 2nd pass: 1.5倍
        for (const sp of shelfProducts) {
            if (!top10Ids.has(sp.productId)) continue;
            const product = productMaster.find(p => p.id === sp.productId);
            if (!product) continue;

            const originalSp = products.find(p => p.productId === sp.productId && p.shelfIndex === sp.shelfIndex);
            if (!originalSp || sp.faceCount !== originalSp.faceCount) continue;

            const newFaceCount = Math.floor(sp.faceCount * 1.5);
            if (newFaceCount <= sp.faceCount) continue;

            const extraNeeded = product.width * (newFaceCount - sp.faceCount);
            const available = targetWidth - currentUsed;

            if (available >= extraNeeded) {
                const oldFace = sp.faceCount;
                sp.faceCount = newFaceCount;
                currentUsed += product.width * (newFaceCount - oldFace);
                warnings.push(`${product.name}: フェイス1.5倍 (${oldFace} → ${sp.faceCount})`);
            }
        }

        let posX = 0;
        for (const sp of shelfProducts) {
            const product = productMaster.find(p => p.id === sp.productId);
            if (!product) continue;

            result.push({
                id: `gen-${sp.id}`,
                productId: sp.productId,
                shelfIndex: sp.shelfIndex,
                positionX: posX,
                faceCount: sp.faceCount,
                isAutoGenerated: true,
                isCut: false,
            });
            posX += product.width * sp.faceCount;
        }

        if (targetWidth - currentUsed > 10) {
            warnings.push(`段${shelfIndex + 1}: ${Math.round(targetWidth - currentUsed)}mmの余白あり`);
        }
    }

    return { products: result, warnings };
}

// ================================================================
// 直接コピー (同幅)
// ================================================================

function applyDirectCopy(
    products: StandardPlanogramProduct[]
): { products: StorePlanogramProduct[]; warnings: string[] } {
    return {
        products: products.map(p => ({
            id: `gen-${p.id}`,
            productId: p.productId,
            shelfIndex: p.shelfIndex,
            positionX: p.positionX,
            faceCount: p.faceCount,
            isAutoGenerated: true,
            isCut: false,
        })),
        warnings: [],
    };
}

