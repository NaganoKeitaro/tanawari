describe('統合パターン: 大中小店舗', () => {
    // ダミーデータと同じ構成のテスト
    const products = [
        makeProduct('beef1', '国産牛テール', 130, 1),
        makeProduct('beef2', '国産牛切り落とし', 120, 2),
        makeProduct('beef3', '国産牛こま切れ', 110, 3),
        makeProduct('pork1', '国産豚バラ薄切り', 120, 11),
        makeProduct('pork2', '国産豚こま切れ', 110, 12),
        makeProduct('chicken1', '鶏もも肉', 130, 21),
    ];

    // 標準棚割: 2段、幅3600mm（12尺）
    const standardProducts: StandardPlanogramProduct[] = [
        // 段0
        makeStdProduct('s1', 'beef1', 0, 0, 3),       // 390mm
        makeStdProduct('s2', 'beef2', 0, 390, 3),      // 360mm
        makeStdProduct('s3', 'beef3', 0, 750, 3),      // 330mm
        makeStdProduct('s4', 'pork1', 0, 1080, 3),     // 360mm
        makeStdProduct('s5', 'pork2', 0, 1440, 3),     // 330mm
        makeStdProduct('s6', 'chicken1', 0, 1770, 3),  // 390mm → 合計2160mm
        // 段1
        makeStdProduct('s7', 'beef1', 1, 0, 3),
        makeStdProduct('s8', 'beef2', 1, 390, 3),
        makeStdProduct('s9', 'beef3', 1, 750, 3),
        makeStdProduct('s10', 'pork1', 1, 1080, 3),
        makeStdProduct('s11', 'pork2', 1, 1440, 3),
        makeStdProduct('s12', 'chicken1', 1, 1770, 3),
    ];
    const standardWidth = 2160;

    it('大型店舗(店舗幅 > 標準): ルールBで拡張', () => {
        const storeWidth = 3600; // > 2160
        const result = applyRuleB(standardProducts, products, storeWidth, standardWidth);

        // 商品数は減らない
        expect(result.products.length).toBeGreaterThanOrEqual(standardProducts.length);

        // top10商品のフェイスが拡張されている
        const beef1s = result.products.filter(p => p.productId === 'beef1');
        expect(beef1s.some(p => p.faceCount > 3)).toBe(true);

        // 各段の幅がstoreWidth以下
        const shelf0 = result.products.filter(p => p.shelfIndex === 0);
        const shelf0Width = shelf0.reduce((sum, sp) => {
            const prod = products.find(p => p.id === sp.productId)!;
            return sum + prod.width * sp.faceCount;
        }, 0);
        expect(shelf0Width).toBeLessThanOrEqual(storeWidth);
    });

    it('小型店舗(店舗幅 < 標準): ルールAでカット', () => {
        const storeWidth = 1500; // < 2160
        const result = applyRuleA(standardProducts, products, storeWidth);

        // 商品数は減る
        expect(result.products.length).toBeLessThanOrEqual(standardProducts.length);

        // 各段の幅がstoreWidth以下
        for (const shelfIdx of [0, 1]) {
            const shelfProducts = result.products.filter(p => p.shelfIndex === shelfIdx);
            const shelfWidth = shelfProducts.reduce((sum, sp) => {
                const prod = products.find(p => p.id === sp.productId)!;
                return sum + prod.width * sp.faceCount;
            }, 0);
            expect(shelfWidth).toBeLessThanOrEqual(storeWidth);
        }

        // ランク最良の商品(beef1, ランク1)は残っている
        expect(result.products.some(p => p.productId === 'beef1')).toBe(true);
    });

    it('同型店舗(店舗幅 = 標準): 直接コピー', () => {
        const result = applyDirectCopy(standardProducts);
        expect(result.products).toHaveLength(standardProducts.length);
        expect(result.warnings).toHaveLength(0);

        // 全て同じ値
        for (let i = 0; i < standardProducts.length; i++) {
            expect(result.products[i].faceCount).toBe(standardProducts[i].faceCount);
        }
    });
});

