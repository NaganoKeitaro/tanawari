describe('シナリオ7: 標準棚割更新後の個店棚割同期', () => {
    const products: Product[] = [
        makeProduct('p1', '商品A', 100, 1, '牛肉'),
        makeProduct('p2', '商品B', 100, 2, '牛肉'),
        makeProduct('p3', '商品C', 100, 5, '豚肉'),
        makeProduct('pNew', '新商品', 100, 3, '牛肉'),
    ];

    it('標準棚割に商品追加後、同期で個店にも反映', () => {
        // 元の標準棚割: p1, p2, p3 → 更新後にpNew追加

        // 更新後の標準棚割: p1, pNew, p2, p3（pNewが追加）
        const updatedStd: StandardPlanogramProduct[] = [
            { id: 's1', productId: 'p1', shelfIndex: 0, positionX: 0, faceCount: 2 },
            { id: 's4', productId: 'pNew', shelfIndex: 0, positionX: 200, faceCount: 2 },
            { id: 's2', productId: 'p2', shelfIndex: 0, positionX: 400, faceCount: 2 },
            { id: 's3', productId: 'p3', shelfIndex: 0, positionX: 600, faceCount: 2 },
        ];

        // 同型店舗での同期 = 直接コピー
        const synced = updatedStd.map(p => ({
            id: `g-${p.id}`, productId: p.productId, shelfIndex: p.shelfIndex,
            positionX: p.positionX, faceCount: p.faceCount, isAutoGenerated: true, isCut: false,
        }));

        // 新商品が含まれている
        expect(synced.some(p => p.productId === 'pNew')).toBe(true);
        expect(synced).toHaveLength(4);
    });

    it('標準棚割から商品削除後、同期で個店からも削除', () => {
        // 元: p1, p2, p3 → 更新後: p1, p2（p3削除）
        const updatedStd: StandardPlanogramProduct[] = [
            { id: 's1', productId: 'p1', shelfIndex: 0, positionX: 0, faceCount: 2 },
            { id: 's2', productId: 'p2', shelfIndex: 0, positionX: 200, faceCount: 2 },
        ];

        const synced = updatedStd.map(p => ({
            id: `g-${p.id}`, productId: p.productId, shelfIndex: p.shelfIndex,
            positionX: p.positionX, faceCount: p.faceCount, isAutoGenerated: true, isCut: false,
        }));

        expect(synced.some(p => p.productId === 'p3')).toBe(false);
        expect(synced).toHaveLength(2);
    });

    it('小型店舗同期: 標準更新後もルールAが正しく適用', () => {
        const updatedStd: StandardPlanogramProduct[] = [
            { id: 's1', productId: 'p1', shelfIndex: 0, positionX: 0, faceCount: 3 },
            { id: 's2', productId: 'p2', shelfIndex: 0, positionX: 300, faceCount: 3 },
            { id: 's3', productId: 'p3', shelfIndex: 0, positionX: 600, faceCount: 3 },
            { id: 's4', productId: 'pNew', shelfIndex: 0, positionX: 900, faceCount: 3 },
        ];
        const storeWidth = 400; // 小型

        const result = applyRuleA(updatedStd, products, storeWidth);

        // 幅に収まっている
        const totalWidth = result.products.reduce((sum, sp) => {
            const prod = products.find(p => p.id === sp.productId)!;
            return sum + prod.width * sp.faceCount;
        }, 0);
        expect(totalWidth).toBeLessThanOrEqual(storeWidth);

        // ランク1(p1)は残っている
        expect(result.products.some(p => p.productId === 'p1')).toBe(true);
    });
});
