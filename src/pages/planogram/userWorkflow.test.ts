describe('シナリオ1: 標準棚割にブロックを配置 → 商品が正しく展開される', () => {
    const beefProducts: ProductPlacement[] = [
        pl('beef1', 0, 0, 3),
        pl('beef2', 0, 390, 3),
        pl('beef1', 1, 0, 3),
        pl('beef2', 1, 390, 3),
    ];
    const porkProducts: ProductPlacement[] = [
        pl('pork1', 0, 0, 2),
        pl('pork1', 1, 0, 2),
    ];

    const beefBlock = { id: 'blk-beef', width: 750, shelfCount: 2, productPlacements: beefProducts };
    const porkBlock = { id: 'blk-pork', width: 240, shelfCount: 2, productPlacements: porkProducts };

    it('全商品にplacedBlockIdが付与される', () => {
        const { products } = buildStandardPlanogramData([beefBlock, porkBlock]);
        expect(products.every(p => p.placedBlockId !== undefined)).toBe(true);
    });

    it('各商品のplacedBlockIdが正しいブロックを指す', () => {
        const { blocks, products } = buildStandardPlanogramData([beefBlock, porkBlock]);

        const beefPlaced = products.filter(p => p.productId.startsWith('beef'));
        const porkPlaced = products.filter(p => p.productId.startsWith('pork'));

        // 牛肉商品は全て牛肉ブロックのplacedBlockId
        expect(beefPlaced.every(p => p.placedBlockId === blocks[0].id)).toBe(true);
        // 豚肉商品は全て豚肉ブロックのplacedBlockId
        expect(porkPlaced.every(p => p.placedBlockId === blocks[1].id)).toBe(true);
    });

    it('商品のpositionXがブロックのオフセット分ずれている', () => {
        const { products } = buildStandardPlanogramData([beefBlock, porkBlock]);

        // 豚肉ブロックの商品は牛肉ブロック幅(750mm)分ずれる
        const porkPlaced = products.filter(p => p.productId === 'pork1');
        expect(porkPlaced.every(p => p.positionX >= 750)).toBe(true);
    });

    it('商品数がブロック内の配置数と一致する', () => {
        const { products } = buildStandardPlanogramData([beefBlock, porkBlock]);
        expect(products).toHaveLength(beefProducts.length + porkProducts.length);
    });
});

