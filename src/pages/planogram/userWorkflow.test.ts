describe('シナリオ2: ブロッククリック → 所属商品の強調表示が正しい', () => {
    const beefBlock = {
        id: 'blk-beef', width: 900, shelfCount: 5,
        productPlacements: [
            pl('beef1', 0, 0, 3),
            pl('beef2', 1, 0, 2),
        ],
    };
    const porkBlock = {
        id: 'blk-pork', width: 900, shelfCount: 5,
        productPlacements: [
            pl('pork1', 0, 0, 3),
            pl('pork2', 1, 0, 2),
        ],
    };

    it('placedBlockIdにより正しいブロックに割り当てられる', () => {
        const { blocks, products } = buildStandardPlanogramData([beefBlock, porkBlock]);
        const masters = [
            { id: 'blk-beef', width: 900, shelfCount: 5 },
            { id: 'blk-pork', width: 900, shelfCount: 5 },
        ];

        // 牛肉ブロック選択時
        const selectedBlockId = blocks[0].id; // pb-blk-beef
        for (const sp of products) {
            const info = findBlockForProduct(sp, blocks, masters);
            const belongsToSelected = info?.placedBlockId === selectedBlockId;

            if (sp.productId.startsWith('beef')) {
                expect(belongsToSelected).toBe(true);  // 牛肉 → 強調
            } else {
                expect(belongsToSelected).toBe(false);  // 豚肉 → dimmed
            }
        }
    });

    it('placedBlockIdがない場合は位置ベースでフォールバック（境界の誤判定あり）', () => {
        const { blocks, products } = buildStandardPlanogramData([beefBlock, porkBlock]);
        const masters = [
            { id: 'blk-beef', width: 900, shelfCount: 5 },
            { id: 'blk-pork', width: 900, shelfCount: 5 },
        ];

        // placedBlockIdを消す
        const productsWithoutBlockId = products.map(p => ({ ...p, placedBlockId: undefined }));

        // 位置ベースでは境界上（positionX=900、ブロック幅900）の商品が前のブロックにマッチしうる
        // → placedBlockIdがないと誤判定する。これがplacedBlockIdが必要な理由
        const beefProds = productsWithoutBlockId.filter(p => p.productId.startsWith('beef'));
        for (const sp of beefProds) {
            const info = findBlockForProduct(sp, blocks, masters);
            expect(info).not.toBeNull();
            expect(info!.blockId).toBe('blk-beef');
        }

        // pork商品はpositionX=900で、beefブロック(0~900)の境界上
        // 位置ベースだと < 900.1 の判定でbeefにマッチしてしまう
        const porkProds = productsWithoutBlockId.filter(p => p.productId.startsWith('pork'));
        for (const sp of porkProds) {
            const info = findBlockForProduct(sp, blocks, masters);
            expect(info).not.toBeNull();
            // 位置ベースでは誤ってblk-beefにマッチする（これがplacedBlockIdの必要性を示す）
            expect(info!.blockId).toBe('blk-beef'); // 意図しない結果だがplacedBlockIdなしの限界
        }
    });

    it('位置ベースでは誤判定するケースをplacedBlockIdで正しく判定', () => {
        // ブロック幅が実際の商品配置幅より大きい場合、位置ベースだと隣のブロック商品を含んでしまう
        const wideBlock = {
            id: 'blk-wide', width: 1200, shelfCount: 5, // 幅1200だが商品は900mm分しかない
            productPlacements: [
                pl('w1', 0, 0, 1),
            ],
        };
        const nextBlock = {
            id: 'blk-next', width: 600, shelfCount: 5,
            productPlacements: [
                pl('n1', 0, 0, 1),
            ],
        };

        const { blocks, products } = buildStandardPlanogramData([wideBlock, nextBlock]);
        const masters = [
            { id: 'blk-wide', width: 1200, shelfCount: 5 },
            { id: 'blk-next', width: 600, shelfCount: 5 },
        ];

        // n1はpositionX=1200で、wideBlockの範囲(0~1200)の境界上
        // placedBlockIdがあるのでblk-nextに正しく割り当てられる
        const n1 = products.find(p => p.productId === 'n1')!;
        const info = findBlockForProduct(n1, blocks, masters);
        expect(info!.blockId).toBe('blk-next');
    });
});

