describe('シナリオ4: 標準棚割 → 個店棚割 自動展開', () => {
    const products: Product[] = [
        makeProduct('beef1', '国産牛テール', 130, 1, '牛肉'),
        makeProduct('beef2', '国産牛切り落とし', 120, 2, '牛肉'),
        makeProduct('pork1', '国産豚バラ薄切り', 120, 10, '豚肉'),
        makeProduct('pork2', '国産豚こま切れ', 110, 15, '豚肉'),
        makeProduct('chicken1', '鶏もも肉', 130, 20, '鶏肉'),
    ];

    // 標準棚割: 5段、幅3600mm（12尺）
    const stdProducts: StandardPlanogramProduct[] = [];
    for (let shelf = 0; shelf < 5; shelf++) {
        let posX = 0;
        for (const prod of products) {
            stdProducts.push({
                id: `std-${shelf}-${prod.id}`,
                productId: prod.id,
                shelfIndex: shelf,
                positionX: posX,
                faceCount: 3,
            });
            posX += prod.width * 3;
        }
    }
    void products.reduce((sum, p) => sum + p.width * 3, 0); // 1830mm per shelf

    it('大型店舗(80尺): ルールBで全商品残り、上位商品が拡張', () => {
        const storeWidth = 24000; // 80尺
        const result = applyRuleB(stdProducts, products, storeWidth);

        // 全商品が残っている
        for (const prod of products) {
            expect(result.products.some(p => p.productId === prod.id)).toBe(true);
        }

        // 上位商品(beef1, ランク1)が拡張されている
        const beef1 = result.products.filter(p => p.productId === 'beef1');
        expect(beef1.some(p => p.faceCount > 3)).toBe(true);
    });

    it('小型店舗(32尺): ルールAでランク下位がカットされる', () => {
        const storeWidth = 1200; // 非常に小さい
        const result = applyRuleA(stdProducts, products, storeWidth);

        // ランク1(beef1)は残っている
        expect(result.products.some(p => p.productId === 'beef1')).toBe(true);

        // 各段の幅がstoreWidth以下
        for (let shelf = 0; shelf < 5; shelf++) {
            const shelfProds = result.products.filter(p => p.shelfIndex === shelf);
            const shelfWidth = shelfProds.reduce((sum, sp) => {
                const prod = products.find(p => p.id === sp.productId)!;
                return sum + prod.width * sp.faceCount;
            }, 0);
            expect(shelfWidth).toBeLessThanOrEqual(storeWidth);
        }
    });

    it('同型店舗: 全商品が1:1コピー', () => {
        const result = {
            products: stdProducts.map(p => ({
                id: `g-${p.id}`, productId: p.productId, shelfIndex: p.shelfIndex,
                positionX: p.positionX, faceCount: p.faceCount, isAutoGenerated: true, isCut: false,
            })),
            warnings: [] as string[],
        };

        expect(result.products).toHaveLength(stdProducts.length);
        for (let i = 0; i < stdProducts.length; i++) {
            expect(result.products[i].faceCount).toBe(stdProducts[i].faceCount);
        }
    });

    it('個店棚割で商品の重複がない', () => {
        const storeWidth = 24000;
        const result = applyRuleB(stdProducts, products, storeWidth);

        // 各段内で同じpositionXに2商品が重ならない
        for (let shelf = 0; shelf < 5; shelf++) {
            const shelfProds = result.products.filter(p => p.shelfIndex === shelf);
            const positions = shelfProds.map(p => p.positionX);
            const uniquePositions = new Set(positions);
            expect(uniquePositions.size).toBe(positions.length);
        }
    });

    it('個店棚割で商品間に不正な隙間がない', () => {
        const storeWidth = 24000;
        const result = applyRuleB(stdProducts, products, storeWidth);

        for (let shelf = 0; shelf < 5; shelf++) {
            const shelfProds = result.products
                .filter(p => p.shelfIndex === shelf)
                .sort((a, b) => a.positionX - b.positionX);

            for (let i = 0; i < shelfProds.length - 1; i++) {
                const prod = products.find(p => p.id === shelfProds[i].productId)!;
                const expectedNextX = shelfProds[i].positionX + prod.width * shelfProds[i].faceCount;
                expect(shelfProds[i + 1].positionX).toBe(expectedNextX);
            }
        }
    });
});

