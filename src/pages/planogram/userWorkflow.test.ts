describe('シナリオ5: buildStandardPlanogramDataの整合性', () => {
    it('ブロック幅の合計とtotalWidthが一致', () => {
        const blocks = [
            { id: 'a', width: 3600, shelfCount: 5, productPlacements: [] as ProductPlacement[] },
            { id: 'b', width: 2400, shelfCount: 5, productPlacements: [] as ProductPlacement[] },
            { id: 'c', width: 1800, shelfCount: 5, productPlacements: [] as ProductPlacement[] },
        ];
        const { totalWidth } = buildStandardPlanogramData(blocks);
        expect(totalWidth).toBe(3600 + 2400 + 1800);
    });

    it('全商品のpositionXがブロック範囲内', () => {
        const blocks = [
            {
                id: 'a', width: 600, shelfCount: 2,
                productPlacements: [
                    pl('p1', 0, 0, 1),
                    pl('p2', 0, 100, 1),
                ],
            },
            {
                id: 'b', width: 400, shelfCount: 2,
                productPlacements: [
                    pl('p3', 0, 0, 1),
                ],
            },
        ];
        const { blocks: stdBlocks, products } = buildStandardPlanogramData(blocks);

        for (const sp of products) {
            const pb = stdBlocks.find(b => b.id === sp.placedBlockId)!;
            const blk = blocks.find(b => `pb-${b.id}` === pb.id)!;
            // 商品のpositionXがブロックの範囲内
            expect(sp.positionX).toBeGreaterThanOrEqual(pb.positionX);
            expect(sp.positionX).toBeLessThan(pb.positionX + blk.width);
        }
    });

    it('ブロック間でplacedBlockIdが正しく分離している', () => {
        const blocks = [
            {
                id: 'a', width: 300, shelfCount: 1,
                productPlacements: [pl('p1', 0, 0, 1)],
            },
            {
                id: 'b', width: 300, shelfCount: 1,
                productPlacements: [pl('p2', 0, 0, 1)],
            },
        ];
        const { blocks: stdBlocks, products } = buildStandardPlanogramData(blocks);

        const p1 = products.find(p => p.productId === 'p1')!;
        const p2 = products.find(p => p.productId === 'p2')!;
        expect(p1.placedBlockId).not.toBe(p2.placedBlockId);
        expect(p1.placedBlockId).toBe(stdBlocks[0].id);
        expect(p2.placedBlockId).toBe(stdBlocks[1].id);
    });
});

