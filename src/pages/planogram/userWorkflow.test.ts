function makeProduct(id: string, name: string, width: number, salesRank: number, dept: string): Product {
    return { id, name, jan: `4900${id}`, width, height: 200, depth: 100, salesRank, category: '', imageUrl: '', departmentName: dept, createdAt: '', updatedAt: '' } as Product;
}

function pl(productId: string, shelfIndex: number, positionX: number, faceCount: number): ProductPlacement {
    return { id: `pl-${productId}-${shelfIndex}`, productId, shelfIndex, positionX, faceCount };
}

function block(id: string, blockId: string, posX: number, posY: number): StandardPlanogramBlock {
    return { id, blockId, positionX: posX, positionY: posY };
}

// ルールA(カット)再現
function applyRuleA(
    products: StandardPlanogramProduct[],
    productMaster: Product[],
    targetWidth: number
): { products: StorePlanogramProduct[]; warnings: string[] } {
    const warnings: string[] = [];
    const sortedProducts = [...products].sort((a, b) => {
        const prodA = productMaster.find(p => p.id === a.productId);
        const prodB = productMaster.find(p => p.id === b.productId);
        return (prodB?.salesRank || 100) - (prodA?.salesRank || 100);
    });
    const result: StorePlanogramProduct[] = [];
    const shelfIndices = [...new Set(sortedProducts.map(p => p.shelfIndex))];

    for (const shelfIndex of shelfIndices) {
        let shelfProducts = sortedProducts.filter(p => p.shelfIndex === shelfIndex);
        let currentWidth = 0;
        for (const sp of shelfProducts) {
            const product = productMaster.find(p => p.id === sp.productId);
            if (!product) continue;
            currentWidth += product.width * sp.faceCount;
        }
        while (currentWidth > targetWidth && shelfProducts.length > 0) {
            const worstProduct = shelfProducts[0];
            const product = productMaster.find(p => p.id === worstProduct.productId);
            if (!product) { shelfProducts = shelfProducts.slice(1); continue; }
            if (worstProduct.faceCount > 1) {
                worstProduct.faceCount -= 1;
                currentWidth -= product.width;
                warnings.push(`${product.name}: フェイス削減`);
            } else {
                shelfProducts = shelfProducts.slice(1);
                currentWidth -= product.width;
                warnings.push(`${product.name}: カット`);
            }
        }
        let posX = 0;
        for (const sp of shelfProducts) {
            const product = productMaster.find(p => p.id === sp.productId);
            if (!product) continue;
            result.push({ id: `g-${sp.id}`, productId: sp.productId, shelfIndex, positionX: posX, faceCount: sp.faceCount, isAutoGenerated: true, isCut: false });
            posX += product.width * sp.faceCount;
        }
    }
    return { products: result, warnings };
}

// ルールB(拡張)再現
function applyRuleB(
    products: StandardPlanogramProduct[],
    productMaster: Product[],
    targetWidth: number
): { products: StorePlanogramProduct[]; warnings: string[] } {
    const warnings: string[] = [];
    const ranked = products.map(p => ({ ...p, salesRank: productMaster.find(pm => pm.id === p.productId)?.salesRank || 100 }))
        .sort((a, b) => a.salesRank - b.salesRank).slice(0, 10);
    const top10Ids = new Set(ranked.map(p => p.productId));
    const shelfIndices = [...new Set(products.map(p => p.shelfIndex))];
    const result: StorePlanogramProduct[] = [];

    for (const shelfIndex of shelfIndices) {
        const shelfProducts = products.filter(p => p.shelfIndex === shelfIndex).map(p => ({ ...p }));
        let currentUsed = 0;
        for (const sp of shelfProducts) {
            const product = productMaster.find(p => p.id === sp.productId);
            if (product) currentUsed += product.width * sp.faceCount;
        }
        for (const sp of shelfProducts) {
            if (!top10Ids.has(sp.productId)) continue;
            const product = productMaster.find(p => p.id === sp.productId);
            if (!product) continue;
            const extraNeeded = product.width * sp.faceCount;
            if (targetWidth - currentUsed >= extraNeeded) {
                const old = sp.faceCount;
                sp.faceCount = old * 2;
                currentUsed += product.width * old;
                warnings.push(`${product.name}: 2倍`);
            }
        }
        let posX = 0;
        for (const sp of shelfProducts) {
            const product = productMaster.find(p => p.id === sp.productId);
            if (!product) continue;
            result.push({ id: `g-${sp.id}`, productId: sp.productId, shelfIndex, positionX: posX, faceCount: sp.faceCount, isAutoGenerated: true, isCut: false });
            posX += product.width * sp.faceCount;
        }
    }
    return { products: result, warnings };
}

// buildStandardPlanogramData再現
function buildStandardPlanogramData(
    blocks: { id: string; width: number; shelfCount: number; productPlacements: ProductPlacement[] }[]
) {
    const stdBlocks: StandardPlanogramBlock[] = [];
    const stdProducts: StandardPlanogramProduct[] = [];
    let posX = 0;

    for (const blk of blocks) {
        const placedBlockId = `pb-${blk.id}`;
        stdBlocks.push({ id: placedBlockId, blockId: blk.id, positionX: posX, positionY: 0 });
        for (const pl of blk.productPlacements) {
            stdProducts.push({
                id: `sp-${placedBlockId}-${pl.productId}-${pl.shelfIndex}`,
                productId: pl.productId,
                shelfIndex: pl.shelfIndex,
                positionX: posX + pl.positionX,
                faceCount: pl.faceCount,
                placedBlockId,
            });
        }
        posX += blk.width;
    }
    return { blocks: stdBlocks, products: stdProducts, totalWidth: posX };
}

// findBlockForProduct再現（placedBlockId優先）
function findBlockForProduct(
    sp: StandardPlanogramProduct,
    sortedBlocks: StandardPlanogramBlock[],
    blockMasters: { id: string; width: number; shelfCount: number }[]
): { placedBlockId: string; blockId: string } | null {
    if (sp.placedBlockId) {
        const pb = sortedBlocks.find(b => b.id === sp.placedBlockId);
        if (pb) return { placedBlockId: pb.id, blockId: pb.blockId };
    }
    // フォールバック: 位置ベース
    for (const pb of sortedBlocks) {
        const master = blockMasters.find(b => b.id === pb.blockId);
        if (!master) continue;
        const inXRange = sp.positionX >= pb.positionX - 0.1 && sp.positionX < pb.positionX + master.width + 0.1;
        const inYRange = sp.shelfIndex >= pb.positionY && sp.shelfIndex < pb.positionY + master.shelfCount;
        if (inXRange && inYRange) return { placedBlockId: pb.id, blockId: pb.blockId };
    }
    return null;
}

