describe('シナリオ3: ブロック入れ替え → 色区分が棚ブロックに紐づいて変わらない', () => {
    const masters: BlockMasterMap = [
        { id: 'blk-beef', width: 900, shelfCount: 5 },
        { id: 'blk-pork', width: 900, shelfCount: 5 },
        { id: 'blk-chicken', width: 600, shelfCount: 5 },
    ];

    // 色はsorted unique blockIdで決まる
    function getBlockColorMap(blocks: StandardPlanogramBlock[]): Map<string, number> {
        const uniqueBlockIds = [...new Set(blocks.map(pb => pb.blockId))].sort();
        const map = new Map<string, number>();
        uniqueBlockIds.forEach((blockId, i) => map.set(blockId, i));
        return map;
    }

    it('入れ替え前後でblockIdごとの色インデックスが同じ', () => {
        const blocks = [
            block('pb1', 'blk-beef', 0, 0),
            block('pb2', 'blk-pork', 900, 0),
            block('pb3', 'blk-chicken', 1800, 0),
        ];

        const colorBefore = getBlockColorMap(blocks);

        // 牛肉と豚肉を入れ替え
        const swapped = swapBlock(blocks, 'pb1', 'right', masters, 2400, 5);
        expect(swapped).not.toBeNull();

        const colorAfter = getBlockColorMap(swapped!);

        // 各blockIdの色インデックスが同じ
        expect(colorAfter.get('blk-beef')).toBe(colorBefore.get('blk-beef'));
        expect(colorAfter.get('blk-pork')).toBe(colorBefore.get('blk-pork'));
        expect(colorAfter.get('blk-chicken')).toBe(colorBefore.get('blk-chicken'));
    });

    it('入れ替え後にplacedBlockIdで再展開しても商品のブロック所属は正しい', () => {
        const blocks = [
            block('pb1', 'blk-beef', 0, 0),
            block('pb2', 'blk-pork', 900, 0),
        ];

        const beefPlacements: ProductPlacement[] = [
            pl('beef1', 0, 0, 3),
        ];
        const porkPlacements: ProductPlacement[] = [
            pl('pork1', 0, 0, 2),
        ];

        // 入れ替え
        const swapped = swapBlock(blocks, 'pb1', 'right', masters, 1800, 5)!;
        expect(swapped).not.toBeNull();

        // 入れ替え後、各ブロックの商品を再展開しplacedBlockIdを付与
        const allProducts: StandardPlanogramProduct[] = [];
        for (const pb of swapped) {
            const placements = pb.blockId === 'blk-beef' ? beefPlacements : porkPlacements;
            const expanded = expandBlockProducts(placements, new Set(['beef1', 'pork1']), pb.positionX, pb.positionY);
            for (const ep of expanded) {
                allProducts.push({ ...ep, id: `sp-${pb.id}`, placedBlockId: pb.id });
            }
        }

        // beef1はblk-beefブロック（pb1）に紐づいている
        const beef1 = allProducts.find(p => p.productId === 'beef1')!;
        const beef1Block = swapped.find(b => b.id === beef1.placedBlockId)!;
        expect(beef1Block.blockId).toBe('blk-beef');

        // pork1はblk-porkブロック（pb2）に紐づいている
        const pork1 = allProducts.find(p => p.productId === 'pork1')!;
        const pork1Block = swapped.find(b => b.id === pork1.placedBlockId)!;
        expect(pork1Block.blockId).toBe('blk-pork');
    });
});

